import { spawnSync } from 'node:child_process';
import { existsSync } from 'node:fs';
import { promises as fs } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const workspaceRoot = path.resolve(__dirname, '..');

const PROTO_ROOT = path.join(workspaceRoot, 'libs', 'grpc', 'src', 'lib');
const PROTO_FILES_DIR = path.join(PROTO_ROOT, 'proto');
const GENERATED_DIR = path.join(
  workspaceRoot,
  'libs',
  'grpc',
  'src',
  'generated',
);
const INDEX_PATH = path.join(workspaceRoot, 'libs', 'grpc', 'src', 'index.ts');
const GRPC_PACKAGE_NAMES_PATH = path.join(
  workspaceRoot,
  'libs',
  'grpc',
  'src',
  'grpc-package-names.ts',
);

/** Matches ts-proto `camelToSnake` for NestJS `*_PACKAGE_NAME` identifiers. */
function camelToSnake(s) {
  return s
    .replace(/[a-z0-9]([A-Z])/g, (m) => `${m[0]}_${m[1]}`)
    .replace(/[A-Z]([A-Z][a-z])/g, (m) => `${m[0]}_${m.substring(1)}`)
    .toUpperCase();
}

/** Same naming as ts-proto when nestJs=true and exportCommonSymbols=true. */
function nestJsPackageConstName(protoPackage) {
  const prefix = camelToSnake(protoPackage.replace(/\./g, '_'));
  return `${prefix}_PACKAGE_NAME`;
}

function resolveBin(name) {
  const binDir = path.join(workspaceRoot, 'node_modules', '.bin');
  const win = process.platform === 'win32';
  const cmdPath = path.join(binDir, `${name}.cmd`);
  const plainPath = path.join(binDir, name);
  if (win && existsSync(cmdPath)) {
    return cmdPath;
  }
  if (existsSync(plainPath)) {
    return plainPath;
  }
  throw new Error(`Missing ${name} in node_modules/.bin (run npm install).`);
}

function toPosix(p) {
  return p.split(path.sep).join('/');
}

async function listProtoRelativePaths() {
  if (!existsSync(PROTO_FILES_DIR)) {
    throw new Error(`Proto directory not found: ${PROTO_FILES_DIR}`);
  }
  const entries = await fs.readdir(PROTO_FILES_DIR, { withFileTypes: true });
  return entries
    .filter((e) => e.isFile() && e.name.endsWith('.proto'))
    .map((e) => path.posix.join('proto', e.name));
}

function runProtoc(protoRelPaths) {
  if (protoRelPaths.length === 0) {
    console.warn(`No .proto files in ${PROTO_FILES_DIR}`);
    return;
  }

  const protoc = resolveBin('protoc');
  const plugin = resolveBin('protoc-gen-ts_proto');
  const protoPathArg = toPosix(path.relative(workspaceRoot, PROTO_ROOT));
  const outArg = toPosix(path.relative(workspaceRoot, GENERATED_DIR));
  const pluginArg = `protoc-gen-ts_proto=${toPosix(plugin)}`;

  const args = [
    `--proto_path=${protoPathArg}`,
    `--plugin=${pluginArg}`,
    `--ts_proto_out=${outArg}`,
    '--ts_proto_opt=nestJs=true',
    // Avoid duplicate exports (e.g. protobufPackage, DeepPartial) when barrel-re-exporting multiple protos.
    '--ts_proto_opt=exportCommonSymbols=false',
    ...protoRelPaths,
  ];

  const result = spawnSync(protoc, args, {
    cwd: workspaceRoot,
    stdio: 'inherit',
    shell: process.platform === 'win32',
    windowsHide: true,
  });

  if (result.error) {
    throw result.error;
  }
  if (result.status !== 0) {
    process.exit(result.status ?? 1);
  }
}

async function walkTsFiles(dir) {
  const entries = await fs.readdir(dir, { withFileTypes: true });
  const files = await Promise.all(
    entries.map(async (entry) => {
      const fullPath = path.join(dir, entry.name);
      if (entry.isDirectory()) {
        return walkTsFiles(fullPath);
      }
      if (!entry.isFile()) {
        return [];
      }
      if (!entry.name.endsWith('.ts')) {
        return [];
      }
      if (entry.name.endsWith('.d.ts')) {
        return [];
      }
      if (entry.name === 'index.ts') {
        return [];
      }
      return [fullPath];
    }),
  );

  return files.flat();
}

async function writeGrpcPackageNames() {
  if (!existsSync(PROTO_FILES_DIR)) {
    return;
  }
  const entries = await fs.readdir(PROTO_FILES_DIR, { withFileTypes: true });
  const protoFiles = entries
    .filter((e) => e.isFile() && e.name.endsWith('.proto'))
    .sort((a, b) => a.name.localeCompare(b.name));

  const lines = [
    '// Auto-generated by scripts/proto-generate-ts.mjs from `package` in each .proto file.',
    '// Nest registration tokens: ts-proto omits these when exportCommonSymbols=false.',
    '',
  ];

  const usedConstNames = new Map();

  for (const ent of protoFiles) {
    const fullPath = path.join(PROTO_FILES_DIR, ent.name);
    const text = await fs.readFile(fullPath, 'utf8');
    const m = text.match(/^\s*package\s+([\w.]+)\s*;/m);
    if (!m) {
      console.warn(
        `[proto-generate-ts] No package declaration in ${ent.name}; skipping PACKAGE_NAME export.`,
      );
      continue;
    }
    const pkg = m[1];
    const constName = nestJsPackageConstName(pkg);
    if (usedConstNames.has(constName)) {
      console.warn(
        `[proto-generate-ts] Duplicate NestJS package const ${constName} from ${ent.name} and ${usedConstNames.get(constName)}.`,
      );
      continue;
    }
    usedConstNames.set(constName, ent.name);
    lines.push(`export const ${constName} = '${pkg}';`);
  }

  lines.push('');
  await fs.writeFile(GRPC_PACKAGE_NAMES_PATH, lines.join('\n'), 'utf8');
}

async function writeGrpcIndex() {
  await fs.mkdir(GENERATED_DIR, { recursive: true });
  const tsFiles = (await walkTsFiles(GENERATED_DIR)).sort((a, b) =>
    a.localeCompare(b),
  );
  const exportLines = tsFiles.map((filePath) => {
    const relative = toPosix(
      path.relative(path.dirname(INDEX_PATH), filePath),
    ).replace(/\.ts$/, '');
    return `export * from './${relative}';`;
  });

  const content = [
    '// Auto-generated by scripts/proto-generate-ts.mjs. Do not edit manually.',
    "export * from './grpc-package-names';",
    ...exportLines,
    '',
  ].join('\n');

  await fs.writeFile(INDEX_PATH, content, 'utf8');
}

async function main() {
  await fs.mkdir(GENERATED_DIR, { recursive: true });
  const protos = await listProtoRelativePaths();
  runProtoc(protos);
  await writeGrpcPackageNames();
  await writeGrpcIndex();
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});