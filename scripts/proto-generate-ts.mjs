import { spawnSync } from 'node:child_process';
import { existsSync } from 'node:fs';
import { promises as fs } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const workspaceRoot = path.resolve(__dirname, '..');

const PROTO_ROOT = path.join(workspaceRoot, 'libs', 'grpc', 'src', 'lib');
const PROTO_FILES_DIR = path.join(PROTO_ROOT, 'proto');
const GENERATED_DIR = path.join(
  workspaceRoot,
  'libs',
  'grpc',
  'src',
  'generated',
);
const INDEX_PATH = path.join(workspaceRoot, 'libs', 'grpc', 'src', 'index.ts');

function resolveBin(name) {
  const binDir = path.join(workspaceRoot, 'node_modules', '.bin');
  const win = process.platform === 'win32';
  const cmdPath = path.join(binDir, `${name}.cmd`);
  const plainPath = path.join(binDir, name);
  if (win && existsSync(cmdPath)) {
    return cmdPath;
  }
  if (existsSync(plainPath)) {
    return plainPath;
  }
  throw new Error(`Missing ${name} in node_modules/.bin (run npm install).`);
}

function toPosix(p) {
  return p.split(path.sep).join('/');
}

async function listProtoRelativePaths() {
  if (!existsSync(PROTO_FILES_DIR)) {
    throw new Error(`Proto directory not found: ${PROTO_FILES_DIR}`);
  }
  const entries = await fs.readdir(PROTO_FILES_DIR, { withFileTypes: true });
  return entries
    .filter((e) => e.isFile() && e.name.endsWith('.proto'))
    .map((e) => path.posix.join('proto', e.name));
}

function runProtoc(protoRelPaths) {
  if (protoRelPaths.length === 0) {
    console.warn(`No .proto files in ${PROTO_FILES_DIR}`);
    return;
  }

  const protoc = resolveBin('protoc');
  const plugin = resolveBin('protoc-gen-ts_proto');
  const protoPathArg = toPosix(path.relative(workspaceRoot, PROTO_ROOT));
  const outArg = toPosix(path.relative(workspaceRoot, GENERATED_DIR));
  const pluginArg = `protoc-gen-ts_proto=${toPosix(plugin)}`;

  const args = [
    `--proto_path=${protoPathArg}`,
    `--plugin=${pluginArg}`,
    `--ts_proto_out=${outArg}`,
    '--ts_proto_opt=nestJs=true',
    ...protoRelPaths,
  ];

  const result = spawnSync(protoc, args, {
    cwd: workspaceRoot,
    stdio: 'inherit',
    shell: process.platform === 'win32',
    windowsHide: true,
  });

  if (result.error) {
    throw result.error;
  }
  if (result.status !== 0) {
    process.exit(result.status ?? 1);
  }
}

async function walkTsFiles(dir) {
  const entries = await fs.readdir(dir, { withFileTypes: true });
  const files = await Promise.all(
    entries.map(async (entry) => {
      const fullPath = path.join(dir, entry.name);
      if (entry.isDirectory()) {
        return walkTsFiles(fullPath);
      }
      if (!entry.isFile()) {
        return [];
      }
      if (!entry.name.endsWith('.ts')) {
        return [];
      }
      if (entry.name.endsWith('.d.ts')) {
        return [];
      }
      if (entry.name === 'index.ts') {
        return [];
      }
      return [fullPath];
    }),
  );

  return files.flat();
}

async function writeGrpcIndex() {
  await fs.mkdir(GENERATED_DIR, { recursive: true });
  const tsFiles = await walkTsFiles(GENERATED_DIR);
  const exportLines = tsFiles
    .map((filePath) => {
      const relative = toPosix(
        path.relative(path.dirname(INDEX_PATH), filePath),
      ).replace(/\.ts$/, '');
      return `export * from './${relative}';`;
    })
    .sort((a, b) => a.localeCompare(b));

  const content = [
    '// Auto-generated by scripts/proto-generate-ts.mjs. Do not edit manually.',
    ...exportLines,
    '',
  ].join('\n');

  await fs.writeFile(INDEX_PATH, content, 'utf8');
}

async function main() {
  await fs.mkdir(GENERATED_DIR, { recursive: true });
  const protos = await listProtoRelativePaths();
  runProtoc(protos);
  await writeGrpcIndex();
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
