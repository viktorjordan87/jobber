import { promises as fs } from 'node:fs';
import path from 'node:path';

const workspaceRoot = process.cwd();
const generatedDir = path.join(
  workspaceRoot,
  'libs',
  'proto',
  'src',
  'generated',
);
const indexPath = path.join(workspaceRoot, 'libs', 'proto', 'src', 'index.ts');

async function walkTsFiles(dir) {
  const entries = await fs.readdir(dir, { withFileTypes: true });
  const files = await Promise.all(
    entries.map(async (entry) => {
      const fullPath = path.join(dir, entry.name);
      if (entry.isDirectory()) {
        return walkTsFiles(fullPath);
      }
      if (!entry.isFile()) {
        return [];
      }
      if (!entry.name.endsWith('.ts')) {
        return [];
      }
      if (entry.name.endsWith('.d.ts')) {
        return [];
      }
      if (entry.name === 'index.ts') {
        return [];
      }
      return [fullPath];
    }),
  );

  return files.flat();
}

async function main() {
  await fs.mkdir(generatedDir, { recursive: true });
  const tsFiles = await walkTsFiles(generatedDir);
  const exportLines = tsFiles
    .map((filePath) => {
      const relative = path
        .relative(path.dirname(indexPath), filePath)
        .replace(/\\/g, '/')
        .replace(/\.ts$/, '');
      return `export * from './${relative}';`;
    })
    .sort((a, b) => a.localeCompare(b));

  const content = [
    '// Auto-generated by scripts/update-proto-index.mjs. Do not edit manually.',
    ...exportLines,
    '',
  ].join('\n');

  await fs.writeFile(indexPath, content, 'utf8');
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
